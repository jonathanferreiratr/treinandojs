var x = 3;

switch(x){
    case 0:
        console.log("x é zero!");
        break;
        case 1:
            console.log("x =1!");
            break;
            case 2:
                console.log("x = 2!");
                break;
                case 3:
                    console.log("x = 3!");
                    break;
                    default:
                        console.log("x não é zero, um, dois ou trés!");
                        break;
}